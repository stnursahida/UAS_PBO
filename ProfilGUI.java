    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:

// TODO add your handling code here:
        try {
            if (txtId.getText().equals("") && txtNama.getText().equals("") && txtAlamat.getText().equals("") && jk.getSelectedItem().equals("") && txtemail.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "silahkan isi data yang akan ditambah");
            } else {
                Profil p = new Profil();
                //     p.setId(Integer.parseInt(txtId.getText()));
                p.setId(txtId.getText());
                p.setNama(txtNama.getText());
                p.setAlamat(txtAlamat.getText());
                p.setJk((String) jk.getSelectedItem());
                p.setEmail(txtemail.getText());
                profileServices.insert(p);
                JOptionPane.showMessageDialog(this, "Data tersimpan");
                ambil_data();
                bersih();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }


    }//GEN-LAST:event_AddActionPerformed

