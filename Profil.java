public class Profil {

    String id;
    String nama;
    String alamat, jk, email;

    public String getId() {
        return id;
    }

    public void setId (String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
    
    public String getJk() {
        return jk;
    }

    public void setJk(String jk) {
        this.jk = jk;
    }
    
    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email= email;
    }
}
